package observer;

/**
 * Интерфейс наблюдателя (Observer) в паттерне Observer.
 * Объекты, реализующие этот интерфейс, подписываются на субъект и реагируют на его изменения.
 */
public interface IObserver {

    /**
     * Вызывается субъектом при каждом изменении его состояния.
     * Наблюдатель может получить актуальное состояние через {@link Subject#getState()}.
     *
     * @param subject ссылка на субъект, который инициировал уведомление
     */
    void update(Subject subject);
}
