package observer;

/**
 * Интерфейс субъекта (Subject) в паттерне Observer.
 * Описывает объект, который ведёт состояние и уведомляет подписанных наблюдателей об изменениях.
 */
public interface Subject {

    /**
     * Уведомить всех зарегистрированных наблюдателей об изменении состояния.
     * Каждый наблюдатель получит вызов {@link IObserver#update(Subject)} с ссылкой на этот субъект.
     */
    void notifyAllObserver();

    /**
     * Подписать наблюдателя на уведомления.
     *
     * @param obs наблюдатель для добавления в список подписчиков
     */
    void attach(IObserver obs);

    /**
     * Отписать наблюдателя от уведомлений.
     *
     * @param obs наблюдатель для удаления из списка подписчиков
     */
    void detach(IObserver obs);

    /**
     * Получить текущее состояние субъекта (например, счётчик секунд).
     *
     * @return текущее значение состояния
     */
    int getState();

    /**
     * Установить состояние субъекта (например, обнулить или задать начальное значение счётчика).
     *
     * @param time новое значение состояния
     */
    void setState(int time);
}
